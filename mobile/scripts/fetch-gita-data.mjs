#!/usr/bin/env node
/**
 * ─────────────────────────────────────────────────────────────────────────
 *  fetch-gita-data.mjs — populate authentic word-by-word + verse audio
 * ─────────────────────────────────────────────────────────────────────────
 *  WHY THIS EXISTS
 *  The KrishnaVerse build sandbox has no open internet, so the authentic
 *  Bhagavad Gita data cannot be downloaded there. Run this ONCE on your own
 *  machine (which has internet). It pulls a public-domain dataset and writes
 *  the generated data files the app imports.
 *
 *  SOURCE (authentic, public domain):
 *    https://github.com/gita/gita  — "Bhagavad Gita in JSON", Unlicense
 *    (public-domain dedication). This is the dataset behind bhagavadgita.io,
 *    maintained by the Ved Vyas Foundation. Each verse object carries a
 *    `word_meanings` string (Sanskrit word — English meaning; ...), plus
 *    Sanskrit `text` and `transliteration`. The repo also ships a
 *    `data/verse_recitation/` folder of authentic audio.
 *
 *  WHAT IT WRITES (idempotent — safe to re-run):
 *    mobile/constants/WordByWord.js     → export const WORD_BY_WORD = {...}
 *    mobile/constants/VerseAudioMap.js  → export const VERSE_AUDIO  = {...}
 *    data/word-by-word.js   (web)       → window.KV_WORD_BY_WORD = {...}
 *    data/verse-audio.js    (web)       → window.KV_VERSE_AUDIO  = {...}
 *
 *  RUN:
 *    cd mobile
 *    node scripts/fetch-gita-data.mjs
 *
 *  Requires Node 18+ (uses the built-in global fetch). No npm install needed.
 * ─────────────────────────────────────────────────────────────────────────
 */

import { writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const MOBILE_DIR = resolve(__dirname, '..');          // .../mobile
const REPO_ROOT = resolve(MOBILE_DIR, '..');          // repo root (web app)

const RAW = 'https://raw.githubusercontent.com/gita/gita/main/data';
const VERSE_JSON_URL = `${RAW}/verse.json`;
const GH_TREE_API =
  'https://api.github.com/repos/gita/gita/git/trees/main?recursive=1';

// ── helpers ────────────────────────────────────────────────────────────────
async function getJSON(url) {
  const res = await fetch(url, { headers: { 'User-Agent': 'krishnaverse-build' } });
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.json();
}

// Parse a `word_meanings` string into [{ w, en }].
// Segments are separated by ';'. Within a segment the Sanskrit word and its
// meaning are separated by an em dash '—' (U+2014); we also tolerate '–' and
// ' - ' as fallbacks. Anything unparseable is skipped (never invented).
function parseWordMeanings(str) {
  if (!str || typeof str !== 'string') return [];
  const out = [];
  for (let seg of str.split(';')) {
    seg = seg.trim();
    if (!seg) continue;
    let idx = seg.indexOf('—');
    if (idx === -1) idx = seg.indexOf('–');
    if (idx === -1) idx = seg.indexOf(' - ');
    if (idx === -1) continue; // can't split safely → skip rather than guess
    const sep = seg[idx] === '—' || seg[idx] === '–' ? 1 : 3;
    const w = seg.slice(0, idx).trim();
    const en = seg.slice(idx + sep).trim();
    if (w && en) out.push({ w, en });
  }
  return out;
}

function writeModule(absPath, varName, obj, kind) {
  mkdirSync(dirname(absPath), { recursive: true });
  const json = JSON.stringify(obj, null, 0);
  const header =
    `// AUTO-GENERATED by mobile/scripts/fetch-gita-data.mjs — do not edit by hand.\n` +
    `// Source: https://github.com/gita/gita (Unlicense / public domain).\n` +
    `// Re-run the script to refresh. Generated ${new Date().toISOString()}.\n`;
  const body =
    kind === 'esm'
      ? `${header}export const ${varName} = ${json};\n`
      : `${header}window.${varName} = ${json};\n`;
  writeFileSync(absPath, body, 'utf8');
}

// ── main ────────────────────────────────────────────────────────────────────
async function main() {
  console.log('→ Fetching verse.json (word meanings)…');
  const verses = await getJSON(VERSE_JSON_URL);
  if (!Array.isArray(verses)) {
    throw new Error('Unexpected verse.json shape (expected an array).');
  }

  // Detect the field names defensively (the schema is stable, but be safe).
  const sample = verses[0] || {};
  const chKey = 'chapter_number' in sample ? 'chapter_number' : 'chapter';
  const vKey = 'verse_number' in sample ? 'verse_number' : 'verse';
  const wmKey =
    'word_meanings' in sample ? 'word_meanings'
    : 'word_meaning' in sample ? 'word_meaning'
    : null;
  if (!wmKey) {
    console.warn(
      '⚠ No word_meanings field found on verse objects. Fields seen:',
      Object.keys(sample).join(', ')
    );
  }

  const wordByWord = {};
  let withWBW = 0;
  for (const v of verses) {
    const ch = v[chKey];
    const vn = v[vKey];
    if (ch == null || vn == null) continue;
    const id = `BG_${ch}_${vn}`;
    const arr = wmKey ? parseWordMeanings(v[wmKey]) : [];
    if (arr.length) {
      wordByWord[id] = arr;
      withWBW++;
    }
  }
  console.log(`  ✓ Parsed word-by-word for ${withWBW} / ${verses.length} verses.`);

  // Verse recitation audio (best effort) — discover files via the GitHub tree.
  const verseAudio = {};
  try {
    console.log('→ Discovering verse_recitation audio via GitHub tree API…');
    const tree = await getJSON(GH_TREE_API);
    const files = (tree.tree || []).filter(
      (t) => t.type === 'blob' && /verse_recitation\/.+\.(mp3|ogg|m4a)$/i.test(t.path)
    );
    for (const f of files) {
      // Expect names like data/verse_recitation/<n>.mp3 where <n> is the global
      // verse id (1..701). Map by id field when available, else by filename num.
      const m = f.path.match(/verse_recitation\/(.+)\.(mp3|ogg|m4a)$/i);
      if (!m) continue;
      const base = m[1];
      const num = parseInt(base.replace(/[^0-9]/g, ''), 10);
      if (!Number.isFinite(num)) continue;
      const verse = verses.find((v) => Number(v.id) === num);
      if (!verse) continue;
      const id = `BG_${verse[chKey]}_${verse[vKey]}`;
      verseAudio[id] = `${RAW}/verse_recitation/${base}.${m[2]}`;
    }
    console.log(`  ✓ Mapped recitation audio for ${Object.keys(verseAudio).length} verses.`);
  } catch (e) {
    console.warn('  ⚠ Audio discovery skipped:', e.message);
    console.warn('    (word-by-word still written; you can add audio URLs later.)');
  }

  // ── write outputs ─────────────────────────────────────────────────────────
  const targets = [
    [resolve(MOBILE_DIR, 'constants/WordByWord.js'), 'WORD_BY_WORD', wordByWord, 'esm'],
    [resolve(MOBILE_DIR, 'constants/VerseAudioMap.js'), 'VERSE_AUDIO', verseAudio, 'esm'],
    [resolve(REPO_ROOT, 'data/word-by-word.js'), 'KV_WORD_BY_WORD', wordByWord, 'global'],
    [resolve(REPO_ROOT, 'data/verse-audio.js'), 'KV_VERSE_AUDIO', verseAudio, 'global'],
  ];
  for (const [p, name, obj, kind] of targets) {
    writeModule(p, name, obj, kind);
    console.log(`  ✓ Wrote ${p.replace(REPO_ROOT + '/', '')} (${Object.keys(obj).length} entries)`);
  }

  console.log('\n✅ Done. Restart Metro with cache clear:  npx expo start -c');
  if (!withWBW) {
    console.log('\n⚠ 0 word-by-word entries parsed — the source schema may have changed.');
    console.log('  Open an issue / paste the first verse object and I\'ll adjust the parser.');
  }
}

main().catch((e) => {
  console.error('\n✗ fetch-gita-data failed:', e.message);
  console.error('  Check your internet connection and try again.');
  process.exit(1);
});
